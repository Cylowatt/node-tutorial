import os from 'os';

// Get Plaforms.
console.log(os.platform());

// CPU Architecture.
console.log(os.arch());

// CPU Core Info.
console.log(os.cpus());

// Free Memory Info.
console.log(os.freemem());

// Total Memory Info.
console.log(os.totalmem());

// Home directory.
console.log(os.homedir());

// Uptime.
console.log(os.uptime());